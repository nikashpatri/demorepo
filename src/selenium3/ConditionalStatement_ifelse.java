package selenium3;

public class ConditionalStatement_ifelse {

	public static void main(String[] args) {
		
		int i=2;
		if(i==1)
		{
			System.out.println(i);
		}
		else if(i==2)
		{
			System.out.println("hello");
		}
		else if (i==3)
		{
			System.out.println(i=3);
		}
		else 
		{
			System.out.println("else block executed");
			
		}
		
	}

}
